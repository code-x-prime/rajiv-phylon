import { api } from "@/lib/axios";

type ApiResponse<T> = { data: T; message?: string; success?: boolean };

function unwrap<T>(res: { data: ApiResponse<T> }): ApiResponse<T> {
  return res.data;
}

// Categories
export type Category = { id: string; name: string; slug: string; imageUrl?: string | null; showOnHome?: boolean; homeOrder?: number };

export const categoriesApi = {
  getAll: () => api.get<ApiResponse<Category[]>>("/categories").then(unwrap).then((r) => r.data ?? []),
  create: (name: string, image?: File, showOnHome?: boolean, homeOrder?: number) => {
    const form = new FormData();
    form.append("name", name);
    if (image) form.append("image", image);
    if (showOnHome !== undefined) form.append("showOnHome", String(showOnHome));
    if (homeOrder !== undefined) form.append("homeOrder", String(homeOrder));
    return api.post<ApiResponse<Category>>("/categories", form, { headers: { "Content-Type": "multipart/form-data" } }).then(unwrap);
  },
  update: (id: string, name: string, image?: File, showOnHome?: boolean, homeOrder?: number) => {
    const form = new FormData();
    form.append("name", name);
    if (image) form.append("image", image);
    if (showOnHome !== undefined) form.append("showOnHome", String(showOnHome));
    if (homeOrder !== undefined) form.append("homeOrder", String(homeOrder));
    return api.put<ApiResponse<Category>>(`/categories/${id}`, form, { headers: { "Content-Type": "multipart/form-data" } }).then(unwrap);
  },
  delete: (id: string) => api.delete<ApiResponse<null>>(`/categories/${id}`).then(unwrap),
};

// SubCategories
export type SubCategory = {
  id: string;
  name: string;
  slug: string;
  categoryId: string;
  imageUrl?: string | null;
  category?: { id: string; name: string; slug: string };
};

export const subCategoriesApi = {
  getAll: () =>
    api.get<ApiResponse<SubCategory[]>>("/subcategories").then(unwrap).then((r) => r.data ?? []),
  getByCategory: (categoryId: string) =>
    api.get<ApiResponse<SubCategory[]>>(`/subcategories/category/${categoryId}`).then(unwrap).then((r) => r.data ?? []),
  getByCategories: async (categoryIds: string[]): Promise<SubCategory[]> => {
    if (!categoryIds.length) return [];
    const arrays = await Promise.all(categoryIds.map((id) => subCategoriesApi.getByCategory(id)));
    const seen = new Set<string>();
    return arrays.flat().filter((s) => {
      if (seen.has(s.id)) return false;
      seen.add(s.id);
      return true;
    });
  },
  create: (name: string, categoryId: string, image?: File) => {
    const form = new FormData();
    form.append("name", name);
    form.append("categoryId", categoryId);
    if (image) form.append("image", image);
    return api.post<ApiResponse<SubCategory>>("/subcategories", form, { headers: { "Content-Type": "multipart/form-data" } }).then(unwrap);
  },
  update: (id: string, data: { name?: string; categoryId?: string }, image?: File) => {
    const form = new FormData();
    if (data.name) form.append("name", data.name);
    if (data.categoryId) form.append("categoryId", data.categoryId);
    if (image) form.append("image", image);
    return api.put<ApiResponse<SubCategory>>(`/subcategories/${id}`, form, { headers: { "Content-Type": "multipart/form-data" } }).then(unwrap);
  },
  delete: (id: string) => api.delete<ApiResponse<null>>(`/subcategories/${id}`).then(unwrap),
};

// Products
export type ProductImage = { id: string; url: string; position: number };
export type CategoryRef = { id: string; name: string; slug?: string };
export type SubCategoryRef = { id: string; name: string; slug?: string };
export type ProductFeatureTag = "NEW_ARRIVAL" | "TRENDING" | "BEST_SELLER" | null;
export type Product = {
  id: string;
  name: string;
  slug: string;
  description: string | null;
  featureTag?: ProductFeatureTag;
  isFeatured?: boolean;
  isNewArrival?: boolean;
  isHighDemand?: boolean;
  showOnHome?: boolean;
  isActive?: boolean;
  createdAt: string;
  images: ProductImage[];
  categories: CategoryRef[];
  subCategories: SubCategoryRef[];
  metaTitle?: string | null;
  metaDescription?: string | null;
  metaKeywords?: string | null;
  ogImage?: string | null;
  seoAutoGenerated?: { metaTitle?: boolean; metaDescription?: boolean; metaKeywords?: boolean; ogImage?: boolean };
};

export const productsApi = {
  getAll: () => api.get<ApiResponse<Product[]>>("/products").then(unwrap).then((r) => r.data ?? []),
  getOne: (slug: string) => api.get<ApiResponse<Product>>(`/products/${slug}`).then(unwrap).then((r) => r.data!),
  getById: (id: string) => api.get<ApiResponse<Product>>(`/products/by-id/${id}`).then(unwrap).then((r) => r.data!),
  create: (data: {
    name: string;
    description?: string;
    categoryIds: string[];
    subCategoryIds?: string[];
    metaTitle?: string;
    metaDescription?: string;
    metaKeywords?: string;
    featureTag?: ProductFeatureTag;
    isFeatured?: boolean;
    isNewArrival?: boolean;
    isHighDemand?: boolean;
    showOnHome?: boolean;
    isActive?: boolean;
  }, images?: File[]) => {
    const form = new FormData();
    form.append("name", data.name);
    form.append("description", data.description ?? "");
    form.append("categoryIds", JSON.stringify(data.categoryIds));
    form.append("subCategoryIds", JSON.stringify(data.subCategoryIds ?? []));
    if (data.metaTitle) form.append("metaTitle", data.metaTitle ?? "");
    if (data.metaDescription) form.append("metaDescription", data.metaDescription ?? "");
    if (data.metaKeywords) form.append("metaKeywords", data.metaKeywords ?? "");
    if (data.featureTag !== undefined && data.featureTag !== null) form.append("featureTag", data.featureTag);
    if (data.isFeatured !== undefined) form.append("isFeatured", String(data.isFeatured));
    if (data.isNewArrival !== undefined) form.append("isNewArrival", String(data.isNewArrival));
    if (data.isHighDemand !== undefined) form.append("isHighDemand", String(data.isHighDemand));
    if (data.showOnHome !== undefined) form.append("showOnHome", String(data.showOnHome));
    if (data.isActive !== undefined) form.append("isActive", String(data.isActive));
    images?.forEach((f) => form.append("images", f));
    return api.post<ApiResponse<Product>>("/products", form, { headers: { "Content-Type": undefined } }).then(unwrap);
  },
  update: (id: string, data: Partial<{
    name: string;
    description: string;
    categoryIds: string[];
    subCategoryIds: string[];
    metaTitle: string;
    metaDescription: string;
    metaKeywords: string;
    featureTag: ProductFeatureTag;
    images: ProductImage[];
    isFeatured: boolean;
    isNewArrival: boolean;
    isHighDemand: boolean;
    showOnHome: boolean;
    isActive: boolean;
  }>, images?: File[]) => {
    const form = new FormData();
    form.append("name", data.name ?? "");
    form.append("description", data.description ?? "");
    form.append("categoryIds", JSON.stringify(data.categoryIds ?? []));
    form.append("subCategoryIds", JSON.stringify(data.subCategoryIds ?? []));
    form.append("metaTitle", data.metaTitle ?? "");
    form.append("metaDescription", data.metaDescription ?? "");
    form.append("metaKeywords", data.metaKeywords ?? "");
    form.append("featureTag", data.featureTag ?? "");
    form.append("isFeatured", String(data.isFeatured ?? false));
    form.append("isNewArrival", String(data.isNewArrival ?? false));
    form.append("isHighDemand", String(data.isHighDemand ?? false));
    form.append("showOnHome", String(data.showOnHome ?? false));
    form.append("isActive", String(data.isActive ?? true));
    form.append("existingImages", JSON.stringify(data.images ?? []));
    images?.forEach((f) => form.append("images", f));
    return api.put<ApiResponse<Product>>(`/products/${id}`, form, { headers: { "Content-Type": undefined } }).then(unwrap);
  },
  delete: (id: string) => api.delete<ApiResponse<null>>(`/products/${id}`).then(unwrap),
  deleteImage: (productId: string, imageId: string) => api.delete<ApiResponse<null>>(`/products/${productId}/images/${imageId}`).then(unwrap),
};

