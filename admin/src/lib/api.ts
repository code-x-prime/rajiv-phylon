// Contact (CRM)
export type InquiryStatus = "PENDING" | "CONTACTED" | "IN_PROGRESS" | "COMPLETED" | "REJECTED";
export type ContactInquiry = {
  id: string;
  name: string;
  phone: string;
  email?: string | null;
  message: string;
  quantity: string | null;
  unit?: string | null;
  productId?: string | null;
  productName?: string | null;
  source?: string | null;
  status: InquiryStatus;
  adminNotes: string | null;
  followUpDate: string | null;
  lastUpdatedById: string | null;
  lastUpdatedBy: { id: string; name: string | null; email: string } | null;
  createdAt: string;
  updatedAt: string;
};

export type ContactInquiriesResponse = {
  data: ContactInquiry[];
  total: number;
  page: number;
  limit: number;
  totalPages: number;
};

export const contactApi = {
  getInquiries: (params: { page?: number; limit?: number; search?: string; status?: string; dateFrom?: string; dateTo?: string }) => {
    const sp = new URLSearchParams();
    if (params.page != null) sp.set("page", String(params.page));
    if (params.limit != null) sp.set("limit", String(params.limit));
    if (params.search) sp.set("search", params.search);
    if (params.status) sp.set("status", params.status);
    if (params.dateFrom) sp.set("dateFrom", params.dateFrom);
    if (params.dateTo) sp.set("dateTo", params.dateTo);
    return api.get<ApiResponse<ContactInquiriesResponse>>(`/contact/inquiries?${sp}`).then(unwrap).then((r) => r.data!);
  },
  updateInquiry: (id: string, data: { status?: InquiryStatus; adminNotes?: string | null; followUpDate?: string | null }) =>
    api.patch<ApiResponse<ContactInquiry>>(`/contact/inquiries/${id}`, data).then(unwrap).then((r) => r.data!),
  deleteInquiry: (id: string) => api.delete<ApiResponse<null>>(`/contact/inquiries/${id}`).then(unwrap),
};
